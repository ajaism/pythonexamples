#This Program demonstartes the functionality of Arithmetic Operators
#aop.py
a = float(input("Enter first number :"))
b = float(input("Enter second number :"))
print("-"*40)
print("Arithmatic operation")
print("sum of {},{}={}".format(a,b,a+b))
print("sub of {},{}={}".format(a,b,a-b))
print("mul of {},{}={}".format(a,b,a*b))
print("div of {},{}={}".format(a,b,a/b))
print("floor div of {},{}={}".format(a,b,a//b))
print("mod of {},{}={}".format(a,b,a%b))
print("expo of {},{}={}".format(a,b,a**b))

